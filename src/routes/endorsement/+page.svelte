<h4 id="endorsement-process" class="mb-2 text-left font-bold">Endorsement Process</h4>

<ol class="mb-4 list-decimal pl-12 text-left text-sm">
	<li class="">
		The candidate requests our endorsement, providing their name, the office they are seeking, and
		their campaign website, other materials, or a candidate statement if they do not have a website
		yet. We do not pursue candidates who do not seek our chapter’s endorsement.
	</li>
	<li class="">
		The candidate is sent our endorsement questionnaire, along with an overview of our endorsement
		criteria and process. The candidate returns the completed questionnaire, to be reviewed by the
		Electoral Working Group and also provided to all DSO members. The Working Group will then use
		the questionnaire in preparation for an interview where we can seek elaboration on responses,
		campaign plans, intentions in office, and other important questions. A recording of the
		interview is provided to all DSO members.
	</li>
	<li class="">
		The Electoral Working Group votes at a meeting to either recommend the candidate for an
		endorsement vote by the membership at the next General Meeting or decline to endorse. Working
		Group votes for endorsement shall be reserved to those who have been members of DSO for at least
		two months or have attended an Electoral Working Group meeting previous to this vote.
	</li>
	<li class="">
		In the event that the Electoral Working Group recommends endorsement, the committee would then
		present a report to the membership at the General Meeting for a final endorsement vote. A 60%
		majority vote by a General Meeting shall be required to secure endorsement. Further, the general
		membership can bypass or overrule the Electoral Working Group and bring a proposal to consider a
		candidate who was rejected by the committee to the floor by the same 60% vote.
	</li>
</ol>

<h4 id="endorsement-process" class="mb-2 text-left font-bold">Endorsement Criteria</h4>

<ol class="mb-4 list-decimal pl-12 text-left text-sm">
	<li class="">
		Candidates must be socialists and members of DSO committed to building the organization as a
		mass working class opposition party, regardless of the ballot line they run on.
	</li>
	<li class="">Candidates must campaign publicly as socialists.</li>
	<li class="">
		Candidates must commit to cross-endorsing and if elected caucusing with any fellow DSO
		endorsees.
	</li>
	<li class="">
		Candidates must commit to participating in at least quarterly meetings with a chapter-level
		Socialists In Office Committee to align their activity in office with DSO’s priorities.
	</li>
	<li class="">
		Candidates must support Hawaiʻi’s right of self determination and oppose the US military
		occupation of the pae ʻāina.
	</li>
	<li class="">
		Candidates must adhere to the following conditions in relation to the Palestinian liberation
		struggle:
	</li>
	<ol>
		<li>Publicly oppose all funding to the State of Israel, including the Iron Dome.</li>
		<li>
			Publicly oppose all criminalization of Anti-Zionism, such as bills advancing the IHRA
			definition which conflates criticism of Israel with antisemitism.
		</li>
		<li>
			Publicly support BDS (Boycott, Divestment, and Sanctions) to end Israeli settler-colonialism.
		</li>
	</ol>
	<li class="">
		Candidates must accept DSA’s <a href="https://platform.dsausa.org/">overall political program</a> and any chapter-level program that DSO
		may adopt. They may not campaign in contradiction to these programs on any issue.
	</li>
	<li class="">
		Candidates must be commensurate partners to our organization, running fully developed campaigns
		with planning, strategy and fundraising potential appropriate for the races they've entered. The
		Electoral Working Group will work with endorsed candidates to develop these competencies, but
		DSO will not endorse candidates who are not aiming to wage effective campaigns that can reach a
		mass working class constituency.
	</li>
</ol>

